            <p>
                Yadnya adalah salah satu ajaran fundamental dalam Agama Hindu, yang berarti persembahan suci atau pengorbanan yang dilakukan secara tulus ikhlas dan penuh kesadaran sebagai wujud syukur dan bakti kepada Tuhan (Ida Sang Hyang Widhi Wasa), leluhur, Rsi, manusia, dan alam semesta.
            </p>

            <div class="detail-item">
                <h4><i class="fas fa-hands-praying" style="color: var(--color-primary); margin-right: 10px;"></i> Hakekat dan Nilai Utama Yadnya</h4>
                <p>
                    Inti dari Yadnya bukanlah kemewahan atau besarnya materi persembahan, melainkan ketulusan hati dan keikhlasan saat melaksanakannya. Tujuannya adalah untuk menjaga keharmonisan kosmos dan melunasi hutang kehidupan (Tri Rna).
                    <br><br>
                    Yadnya yang dilakukan tanpa pamrih dikenal sebagai Nishkama Karma dan merupakan jalan untuk mencapai kedamaian spiritual.
                </p>
            </div>

            <div class="detail-item">
                <h4><i class="fas fa-church" style="color: var(--color-primary); margin-right: 10px;"></i> Panca Yadnya (Lima Jenis Persembahan Suci)</h4>
                <p>
                    Konsep Yadnya diklasifikasikan menjadi lima jenis utama berdasarkan penerima persembahan, yang wajib dilaksanakan oleh umat Hindu untuk menyeimbangkan hidup:
                    <ul>
                        <li>Dewa Yadnya: Persembahan dan pengorbanan suci kepada Tuhan (Dewa/Bhatara).</li>
                        <li>Rsi Yadnya: Persembahan suci kepada para Rsi, guru suci, dan orang bijak.</li>
                        <li>Pitra Yadnya:Persembahan suci kepada leluhur (roh orang tua/pendahulu).</li>
                        <li>Manusa Yadnya:Persembahan suci untuk penyucian, kesejahteraan, dan kesempurnaan hidup manusia.</li>
                        <li>Bhuta Yadnya:Persembahan suci kepada alam semesta dan makhluk bawahan (Bhuta Kala) untuk menjaga keseimbangan lingkungan.</li>
                    </ul>
                </p>
