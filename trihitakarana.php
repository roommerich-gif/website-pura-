            </p>

            <div class="detail-item">
                <h4><i class="fas fa-church" style="color: var(--color-primary); margin-right: 10px;"></i> Parahyangan (Hubungan dengan Tuhan)</h4>
                <p>
                    Ini adalah hubungan spiritual antara manusia dengan Sang Hyang Widhi Wasa (Tuhan Yang Maha Esa) beserta manifestasi-Nya. Realisasi Parhyangan dilakukan melalui kegiatan keagamaan, seperti persembahyangan, upacara yadnya, dan pembangunan serta pemeliharaan Pura. Di Pura Mandira Taman Sari, hubungan ini diwujudkan melalui setiap kegiatan persembahyangan rutin dan hari raya besar.
                </p>
            </div>

            <div class="detail-item">
                <h4><i class="fas fa-users" style="color: var(--color-primary); margin-right: 10px;"></i> Pawongan (Hubungan dengan Sesama Manusia)</h4>
                <p>
                    Hubungan sosial yang harmonis antar sesama manusia. Prinsip ini diwujudkan melalui sikap kasih sayang, toleransi, gotong royong (ngayah), musyawarah, dan saling menghormati di antara anggota komunitas Pura dan masyarakat luas. Pawongan menjadi pilar persatuan dan kerukunan umat.
                </p>
            </div>
            
            <div class="detail-item">
